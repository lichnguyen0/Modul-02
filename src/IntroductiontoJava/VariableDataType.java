package IntroductiontoJava;

public class VariableDataType {
    public static void main(String[] args) {
        int i = 10; // khai bao bien i la kieu int co gia tri la 10
        float f = 20.5f; // khai bao bien f co kieu la float co gia tri la 20.5
        double d = 20.5; // khai bao bien d co kieu la double co gia tri la 2-.5
        boolean b = true; //  khai bao bien d  co kieu la boolean co gia tri true
        char c = 'a'; // khai bao bien c co kieu char co gia tri la ky tu a
        String s = "Hà nội"; // khai bao bien s co kieu là String co gia tri la chuoi Hà Nội

        System.out.println("i = "+ i);
        System.out.println("f = "+ f);
        System.out.println("d = "+ d);
        System.out.println("b = "+ b);
        System.out.println("c = "+ c);
        System.out.println("s = "+ s);
    }
}
